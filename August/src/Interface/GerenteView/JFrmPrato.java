    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBtnAddActionPerformed
    {//GEN-HEADEREND:event_jBtnAddActionPerformed
        // TODO add your handling code here:
        if(!jTxtQuantidade.getText().isEmpty())//Testa e adiciona uma linha a tabela
        {
            cont++;
            mod =(DefaultTableModel)jTblListaIngredientes.getModel();
            mod.addRow(new Object[]{cont, jTxtQuantidade.getText(), jCmbNomeIngrediente.getSelectedItem()});
            ingredientes.add((Ingrediente)lista.get(jCmbNomeIngrediente.getSelectedIndex()));
            quantidades.add(Float.parseFloat(jTxtQuantidade.getText()));
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt)//GEN-FIRST:event_formWindowOpened
    {//GEN-HEADEREND:event_formWindowOpened
        // TODO add your handling code here:
        limpar();
        int posi = jCmbNomeIngrediente.getSelectedIndex();
        try
        {
           lista = dI.carrega();
           jCmbNomeIngrediente.removeAllItems();
           for(int ind=0; ind < lista.size(); ind++) 
           {
               Object o = lista.get(ind);
               Ingrediente i = (Ingrediente) o;
               jCmbNomeIngrediente.addItem(i.getNome()); 
           }
           jCmbNomeIngrediente.setSelectedIndex(-1);
           if (posi > -1) 
             jCmbNomeIngrediente.setSelectedIndex(posi);
        } 
        catch(Exception e)
        {
            Diversos.mostrarDados("Problemas ao carregar, inexistência de ingredientes" +
                    e.getMessage(), "Another Roman Leader", false); 
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

