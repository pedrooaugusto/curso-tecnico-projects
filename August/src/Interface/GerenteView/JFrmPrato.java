    public void limpar()
    {
        jTxtID.setText("");
        jTxtID.setEditable(true);
        jTxtNome.setText("");
        jCmBSessao.setSelectedIndex(-1);
        jTxtPreco.setText("");
        jTxtAreaModoPreparo.setText("");
        jCmbNomeIngrediente.setSelectedIndex(-1);
        jBtnIncluir.setEnabled(false);
        jBtnAlterar.setEnabled(false);
        jBtnExcluir.setEnabled(false);
        jTxtNome.setText("");
        jTxtID.requestFocusInWindow();
        clearTable();
        ingredientes.clear();
        quantidades.clear();
        jLblUni.setText("");
        jTxtQuantidade.setText("");
        cont = 0;
    }
    public void clearTable()
    {
        mod =(DefaultTableModel)jTblListaIngredientes.getModel();
        for(int i = mod.getRowCount(); i > 0; i--)
        {
            mod.removeRow(i-1);
        }
    }
    public void carregarObjeto(Prato p)
    {
        clearTable();
        jTxtID.setText(String.valueOf(p.getID()));
        jTxtNome.setText(p.getNome());
        jCmBSessao.setSelectedItem(p.getSecao());
        jTxtPreco.setText(NumberFormat.getCurrencyInstance().format(p.getPreco()));
        jTxtAreaModoPreparo.setText(p.getModoPreparo());
        mod = (DefaultTableModel)jTblListaIngredientes.getModel();
        for(int i = 0; i < p.getIngrediente().size(); i++)
        {
            mod.addRow(new Object[]{i+1, p.getQuantidade(i)+" "+p.getIngrediente(i).getUnidadeMedida(),
                      p.getIngrediente(i).getNome()});
        }
    }
    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBtnIncluirActionPerformed
    {//GEN-HEADEREND:event_jBtnIncluirActionPerformed
        // TODO add your handling code here:
        Prato p;
        int id;
        if(!jTxtID.getText().isEmpty()
            && !jTxtNome.getText().isEmpty()
            && jCmBSessao.getSelectedIndex() > -1
            && !jTxtPreco.getText().isEmpty()
            && !jTxtAreaModoPreparo.getText().isEmpty()
            && jTblListaIngredientes.getRowCount() > 0
        )
        {
            p = new Prato();
            p.setID(Integer.parseInt(jTxtID.getText()));
            p.setNome(jTxtNome.getText());
            p.setSecao(jCmBSessao.getSelectedItem().toString());
            String preco = jTxtPreco.getText().replace(",", ".");
            preco = preco.replace("R$ ", "");
            p.setPreco(Float.parseFloat(preco));
            p.setModoPreparo(jTxtAreaModoPreparo.getText());
            p.setIngrediente(ingredientes);
            p.setQuantidade(quantidades);
            if(jBtnIncluir.isEnabled())
            {
                if (dP.incluir(p))
                { //==true
                    Diversos.mostrarDados("Prato foi inserido com sucesso", "'-'", true);
                    jBtnAlterar.setEnabled(true);
                    jBtnExcluir.setEnabled(true);
                }
                else // ==false
                {
                    Diversos.mostrarDados("Problemas ao inserir os dados do prato)", titulo, false);
                }
            }
            else
            {
                if(dP.alterar(p))
                {
                    Diversos.mostrarDados("Dados do prato foram atualizados com sucesso", titulo, true);
                }
                else
                {
                    Diversos.mostrarDados("Problemas ao atualizar os dados do prato", "Its a new World", false);
                    jBtnAlterar.setEnabled(false);
                    jBtnIncluir.setEnabled(false);
                }
            }
            jBtnIncluir.setEnabled(false);
        }
        else
        {
            Diversos.mostrarDados("Preencha todos os campos", "42!", false);
        }
    }//GEN-LAST:event_jBtnIncluirActionPerformed

