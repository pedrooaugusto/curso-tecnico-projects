    public void carregarObjeto(Lote l)
    {
        
        jTxtNumeroLote.setText(String.valueOf(l.getNumero()));
        jCmbNomeProduto.removeAllItems();
        jCmbNomeProduto.addItem(l.getIngrediente().getNome());
        jCmbNomeProduto.setSelectedIndex(0);
        jTxtQuantidadeAtual.setText(String.valueOf(l.getQuantidadeAtual()));
        jTxtQuantidadeIncial.setText(String.valueOf(l.getQuantidadeInicial()));
        jFtdTxtValidade.setText(Diversos.parseBrazil(l.getValidade()));
        jLblUni.setText(l.getIngrediente().getUnidadeMedida());
        jLblUni2.setText(l.getIngrediente().getUnidadeMedida());
        jTxtIDProduto.setText(String.valueOf(l.getIngrediente().getID()));
        oldLote = l;
    }
    private void jTxtNumeroLoteFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_jTxtNumeroLoteFocusLost
    {//GEN-HEADEREND:event_jTxtNumeroLoteFocusLost
        Lote l = null;
        int numero;
        boolean sucesso = true;//Obteu sucesso em carregar os produtos
        if(!Diversos.testaNum(jTxtNumeroLote.getText(), titulo))
        {
           jTxtNumeroLote.setText("");
           jTxtNumeroLote.requestFocusInWindow();
        }
        else
        { 
            numero = Integer.parseInt(jTxtNumeroLote.getText());
            if(!Diversos.intervalo(numero, 0, 0, titulo))
            {
                jTxtNumeroLote.setText("");
                jTxtNumeroLote.requestFocusInWindow();
            }
            else
            {
                l = dL.busca(numero, 0);
                if (l == null)
                {
                    novo = true;
                    jBtnIncluir.setEnabled(true);
                    Diversos.mostrarDados("Lote inexistente",titulo,false);
                    jTxtQuantidadeIncial.setEditable(false);
                    sucesso = carregarProdutos();
                }    
                else
                {  
                    novo = false;
                    carregarObjeto(l);
                    jBtnAlterar.setEnabled(true);
                    jBtnExcluir.setEnabled(true);
                    jTxtQuantidadeIncial.setEditable(true);
                }
                if(sucesso)
                {
                    jTxtNumeroLote.setEditable(false);
                }
            } 
        }
    }//GEN-LAST:event_jTxtNumeroLoteFocusLost

