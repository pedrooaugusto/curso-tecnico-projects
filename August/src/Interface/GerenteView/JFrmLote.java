    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBtnIncluirActionPerformed
    {//GEN-HEADEREND:event_jBtnIncluirActionPerformed
        // TODO add your handling code here:
        Lote l;
        Ingrediente i = null;
        int id;
        if(
            !jTxtNumeroLote.getText().isEmpty()
            && jCmbNomeProduto.getSelectedIndex() > -1
            && !jTxtQuantidadeAtual.getText().isEmpty()
            && !jTxtQuantidadeIncial.getText().isEmpty()
            && !jFtdTxtValidade.getText().isEmpty()
        )
        {
            l = new Lote();
            id = Integer.parseInt((String)jTxtIDProduto.getText());
            l.setNumero(Integer.parseInt(jTxtNumeroLote.getText()));
            l.setQuantidadeAtual(Float.parseFloat(jTxtQuantidadeAtual.getText()));
            l.setQuantidadeInicial(Float.parseFloat(jTxtQuantidadeIncial.getText()));
            l.setValidade(Diversos.parseIngles(jFtdTxtValidade.getText()));
            Object o = dI.busca(id, 0);
            i = (Ingrediente) o;
            l.setIngrediente(i);
            if(jBtnIncluir.isEnabled())
            {
                if (dL.incluir(l))
                { //==true
                    Diversos.mostrarDados("Dados do lote foi inserido com sucesso", "I can see for miles", true);
                    jBtnAlterar.setEnabled(true);
                    jBtnExcluir.setEnabled(true);
                }
                else // ==false
                {
                    Diversos.mostrarDados("Problemas ao inserir os dados do lote)", titulo, false);
                }
            }
            else
            {
                if(dL.alterar(l))
                {
                    Diversos.mostrarDados("Dados do lote foram atualizados com sucesso", titulo, true);
                    if(oldLote.getQuantidadeAtual() >= l.getQuantidadeAtual())//Conta de subtraÃ§Ã£o para Atualizar o Estoque Atual
                    {
                        dL.atualizarEstoque("out", oldLote.getQuantidadeAtual() - l.getQuantidadeAtual(), oldLote);
                    }
                    else
                    {
                        dL.atualizarEstoque("in", l.getQuantidadeAtual() - oldLote.getQuantidadeAtual(), l);
                    }
                }
                else
                {
                    Diversos.mostrarDados("Problemas ao atualizar os dados do lote", "Taxman!", false);
                    jBtnAlterar.setEnabled(false);
                    jBtnIncluir.setEnabled(false);
                }
            }
            jBtnIncluir.setEnabled(false);
        }
        else
        {
            Diversos.mostrarDados("Preencha todos os campos", "Doctor Robert", false);
        }
    }//GEN-LAST:event_jBtnIncluirActionPerformed

