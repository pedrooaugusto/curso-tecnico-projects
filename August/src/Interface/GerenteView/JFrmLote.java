    public boolean carregarProdutos()/*
                                        Essa funÃ§Ã£o surgiu pq eu nÃ£o estava pensando bem no momento...
                                        Enfim, ela sugiu pq eu nÃ£o queria povoar o combo box assim que
                                        a janela fosse aberta...
                                    */
    {
        int posi = jCmbNomeProduto.getSelectedIndex();
        boolean ok = true;
            try
            {
               lista = dI.carrega();
               jCmbNomeProduto.removeAllItems();
               for(int ind=0; ind < lista.size(); ind++) 
               {
                   Object o = lista.get(ind);
                   Ingrediente i = (Ingrediente) o;
                   jCmbNomeProduto.addItem(i.getNome()); 
               }
               jCmbNomeProduto.setSelectedIndex(-1);
               jTxtIDProduto.setText("");
               jLblUni.setText("");
               jLblUni2.setText("");
               if (posi > -1) 
                 jCmbNomeProduto.setSelectedIndex(posi);
               if(lista.size() == 0)
               {
                  Diversos.mostrarDados("Cadastre produtos primeiro!", "uÃ©", false);
                  ok = false;
                  limpar();
                }
            } 
            catch(Exception e)
            {
                ok = false;
                Diversos.mostrarDados("Problemas ao carregar a lista: DaoIngrediente.carrega()" +
                        e.getMessage(), titulo, false); 
                e.printStackTrace();
            }
        return ok;
    }
