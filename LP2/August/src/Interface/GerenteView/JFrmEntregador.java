    public void carregarObjeto(Entregador e)
    {
        
        jTxtID.setText(String.valueOf(e.getID()));
        jTxtNome.setText(e.getNome());
        jTxtSalario.setText(NumberFormat.getCurrencyInstance().format(e.getSalario()));
        jFtdTxtData.setText(e.getDataNascimento());
        String a = (String) e.getVeiculo();//U√©
        jCmbVeiculo.setSelectedItem(a);
        jFtdTxtCPF.setText(e.getCpf());
        if(!e.getFotoURL().equals("blue jay way"))//N√£o tem foto
        {
            if(new File(e.getFotoURL()).exists())
                jLblFoto.setIcon(toScala(new ImageIcon(e.getFotoURL())));
            else
                Diversos.mostrarDados(e.getFotoURL()+"\nN√£o encontrado", titulo, false);
        }
        else
        {
            jLblFoto.setText("CARREGAR IMG");
        }
    }    
