    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIncluirActionPerformed
        // TODO add your handling code here:
        Entregador e;
        int id;
        if(!jTxtID.getText().isEmpty()
            && jCmbVeiculo.getSelectedIndex() > -1
            && !jTxtNome.getText().isEmpty()
            && !jTxtSalario.getText().isEmpty()
            && !jFtdTxtData.getText().isEmpty()
        )
        {
            e = new Entregador();
            e.setID(Integer.parseInt(jTxtID.getText()));
            e.setNome(jTxtNome.getText());
            e.setVeiculo((String)jCmbVeiculo.getSelectedItem());
            e.setDataNascimento(jFtdTxtData.getText());
            e.setFotoURL((url.isEmpty() ? "blue jay way" : url));
            String preco = jTxtSalario.getText().replace(",", ".");
            preco = preco.replace("R$ ", "");
            try{
                e.setSalario(Float.parseFloat(preco));
            }catch(Exception ex){
                Diversos.mostrarDados("Nao converteu o preco. Tire as virgulas", titulo, false);
                return;
            }
            e.setCpf(jFtdTxtCPF.getText());
            if(jBtnIncluir.isEnabled())
            {
                if (dE.incluir(e))
                { //==true
                    Diversos.mostrarDados("Entregador foi inserido com sucesso", "hey you", true);
                    jBtnAlterar.setEnabled(true);
                    jBtnExcluir.setEnabled(true);
                }
                else // ==false
                {
                    Diversos.mostrarDados("Problemas ao inserir os dados do entregador)", titulo, false);
                }
            }
            else
            {
                if(dE.alterar(e))
                {
                    Diversos.mostrarDados("Dados do entregador foram atualizados com sucesso", titulo, true);
                }
                else
                {
                    Diversos.mostrarDados("Problemas ao atualizar os dados do entregador", titulo, false);
                    jBtnAlterar.setEnabled(false);
                    jBtnIncluir.setEnabled(false);
                }
            }
            jBtnIncluir.setEnabled(false);
        }
        else
        {
            Diversos.mostrarDados("Preencha todos os campos", "The Blue Box", false);
        }
    }//GEN-LAST:event_jBtnIncluirActionPerformed

