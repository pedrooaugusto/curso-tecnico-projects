    public static void setLimite()
    {
        String lim = Diversos.leDados("Selecione o tempo de sincronismo com"
                                            + " o servidor:\n.15 Segundos\n.20 Segundos (Recomendado)"
                                            + "\n.40 Segundos\n.60 Segundos", "Tempo de sincronismo");
        if(Diversos.testaNum(lim, "Exception"))
        {
            limit = Integer.parseInt(lim);
        }
        else
        {
            Diversos.mostrarDados("Tempo de sincroniza setado para 20s", "22:04", true);
            limit = 20;
        }
        attTable().start();
    }
