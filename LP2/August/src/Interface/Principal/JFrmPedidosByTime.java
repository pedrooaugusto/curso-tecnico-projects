    public static void sincronizarBanco()//Reefazer a consulta no banco
    {
        dtm = dC.retornaLista(14, "");
        jTblPedidos.setModel(dtm);
        dataIni = new String[dtm.getRowCount()];
        for (int i = 0; i < dataIni.length; i++)
        {
            dataIni[i] = (String) dtm.getValueAt(i, 5);
        }
    }
    public static Thread attTable()//Atualiza a tabela de 1 em 1 segundo
    {
        Thread t = new Thread()
        {
            public void run()
            {
                go = true;
                while (go)
                {
                    try
                    {
                        if (secs == limit)//Se ja se passou o tempo limite refaz a consulta
                        {
                            sincronizarBanco();
                            customizeTable();
                            secs = 0;
                        }
                        Thread.sleep(1000);
                        for (int i = 0; i < dataIni.length; i++)
                        {
                            dtm.setValueAt(Diversos.tempoDecorrido(dataIni[i]), i, 5);
                        }
                        secs++;
                    } catch (Exception ex)
                    {
                        //ex.printStackTrace();
                    }
                }
            }
        };
        return t;
    }
    public static void customizeTable()//Pinta a tabela
    {
        for (int i = 0; i < 6; i++)
        {
            jTblPedidos.getColumnModel().getColumn(i).setCellRenderer(tdRenderer);
            jTblPedidos.getColumnModel().getColumn(i).setHeaderRenderer(new CustomCabecalhoJTable(2));
        }
        jTblPedidos.setCellSelectionEnabled(true);
        jTblPedidos.setSelectionBackground(Color.BLUE);
    }
