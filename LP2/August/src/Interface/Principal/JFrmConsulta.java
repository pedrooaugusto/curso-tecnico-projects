    private void formWindowOpened(java.awt.event.WindowEvent evt)//GEN-FIRST:event_formWindowOpened
    {//GEN-HEADEREND:event_formWindowOpened
        // TODO add your handling code here:
        int posiI = jCmbIngrediente.getSelectedIndex();
        try
        {
           listaI = dI.carrega();
           jCmbIngrediente.removeAllItems();
           jCmbIngrediente.addItem("Todos");
           for(int ind=0; ind < listaI.size(); ind++) 
           {
               Object o = listaI.get(ind);
               Ingrediente i = (Ingrediente) o;
               jCmbIngrediente.addItem(i.getNome()); 
           }
           if (posiI > -1) 
             jCmbIngrediente.setSelectedIndex(posiI);
        } 
        catch(Exception e)
        {
            Diversos.mostrarDados("Problemas ao carregar, inexistência de ingredientes" +
                    e.getMessage(), "Another Roman Leader", false); 
            e.printStackTrace();
        }
        int posiE = jCmbEntregador.getSelectedIndex();
        try
        {
            listaE = dE.carrega();
            jCmbEntregador.removeAllItems();
            jCmbEntregador.addItem("Todos");
            for (int ind = 0; ind < listaE.size(); ind++)
            {
                Object o = listaE.get(ind);
                Entregador en = (Entregador) o;
                jCmbEntregador.addItem(en.getNome());
            }
            if (posiE > -1)
            {
                jCmbEntregador.setSelectedIndex(posiE);
            }
        } catch (Exception e)
        {
            Diversos.mostrarDados("Problemas ao carregar, inexistência de entregadores"
                    + e.getMessage(), "19:39", false);
            e.printStackTrace();
        }
        init = false;
    }//GEN-LAST:event_formWindowOpened

