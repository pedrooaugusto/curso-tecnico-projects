    public void selecionarTipo(int op)//Ativa os comboBox de acordo com o tipo de consulta
    {
        switch(op)
        {
            case 1:
                jCmbMes.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbIngrediente.setEnabled(false);
                break;
            case 2:
                jCmbMes.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(true);
                jCmbIngrediente.setEnabled(false);
                break;
            case 3:
                jCmbMes.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbIngrediente.setEnabled(true);
                break;
            case 4:
                jCmbMes.setEnabled(false);
                jCmbIngrediente.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbEntregador.setEnabled(true);
                break;
            case 5:
                jCmbIngrediente.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbMes.setEnabled(true);
                break;
            case 6:
                jCmbMes.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbIngrediente.setEnabled(false);
                break;
            case 7:
                jCmbMes.setEnabled(false);
                jCmbEntregador.setEnabled(false);
                jCmbSessao.setEnabled(false);
                jCmbIngrediente.setEnabled(false);
                break;
        }
    }
    public void customizeTable()//Coloca cor na tabela
    {
        for(int i = 0; i < jTblConsulta.getColumnModel().getColumnCount(); i++)
        {
            jTblConsulta.getColumnModel().getColumn(i).setCellRenderer(tdRenderer);
            jTblConsulta.getColumnModel().getColumn(i).setHeaderRenderer(new CustomCabecalhoJTable(1));
        }
        jTblConsulta.setCellSelectionEnabled(true);
        jTblConsulta.setSelectionBackground(Color.BLUE);
    }
